def localize_file(path: Path, mapping: dict[str, str]) -> bool:
    text = path.read_text(encoding="utf-8")
    original = text
    for source, target in sorted(mapping.items(), key=lambda item: len(item[0]), reverse=True):
        text = text.replace(source, target)
    if text != original:
        path.write_text(text, encoding="utf-8", newline="\n")
        return True
    return False


def main() -> None:
    changed = 0
    for locale, mapping in LOCALE_TEXT.items():
        locale_dir = PAGES / locale
        if not locale_dir.exists():
            continue
        for path in locale_dir.rglob("*.astro"):
            if localize_file(path, mapping):
                changed += 1
    print(f"Localized static page text in {changed} files.")


if __name__ == "__main__":
    main()
